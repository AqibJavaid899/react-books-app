import { toast } from "react-toastify";

export const darkReactToastify = (message) => {
  return toast.dark(message, {
    position: "top-right",
    autoClose: 2000,
    hideProgressBar: true,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
  });
};

